							<h5>Lundi 14/01/2019</h5>
							<p>Bonjour tout le monde, nous sommes arrivés ce matin vers 7h15 sous les flocons blancs, aux flocons verts, après une nuit passée dans le car, durant laquelle, certains ont dormi, d’autres ont essayé mais sans succès et d’autres enfin, avaient décidé dès le début, de ne pas fermer l’œil de la nuit. <br><br>

							La matinée fut rythmée par la prise de possession des chambres, suivie d’un petit déjeuner copieux, d’un briefing rapide avant de récupérer le matériel de ski. Un petit temps libre boules de neige et luge clôturait cette matinée riche en nouveauté. <br><br>

							Visites : <br><br>

							Pour les élèves de TU, nous avons visités ADVITIS, qui est une petite société (environ 12 salariés) spécialisée dans le domaine du décolletage (usinage de petite pièce Ø1 à 12 mm). L’entreprise travaille pour différents domaines tel que le spatial, l’aéronautique mais aussi l’horlogerie. Il y avait différents types de machines comme des machines à commande numérique mais aussi des machines à came  Nous avons aussi vu la partie contrôle qualité de l’entreprise avec le fonctionnement de ses différents moyens de contrôle. C’était une visite enrichissante sur le point culturel mais aussi au niveau technique (avec les différents moyens de l’entreprise). <br><br>

							Les élèves de Maintenance des Equipements Industriels ont pu visiter la société SOMFY, accueilli par la DRH, cette personne a pu nous recevoir dans une salle et nous faire visiter la chaîne des nouveaux moteurs de diamètre 40mm, nous avons rencontré des techniciens de maintenance en opération curative.</p> <br><br>
							<h5>Mardi 15/01/2019</h5>
							<p>Cette journée fut consacrée au ski. <br><br>

							Pour les débutants,  pour la plupart, nous avons découvert les sensations du ski et des équipements  (chaussures, casque, masque et les skis eux-mêmes). Les chaussures sont très inconfortables et il est très difficile de marcher avec car elles sont très rigide et serrées. Elles empêchent les pieds et surtout les chevilles de bouger. <br><br>

							Lors du cours de ski les plus grandes difficultés ont été de comprendre comment se déplacer, négocier les virages et surtout de ralentir ou s’arrêter  sans tomber. <br><br>

							En fin de matinée, la fatigue commençait à se faire ressentir, les chaussures nous faisaient mal aux tibias et aux chevilles. <br><br>

							Nous tombions plus souvent et facilement car nous attaquions une nouvelle piste avec une descente  plus raide, avec des bosses et des virages plus serrés. <br><br>

							Sinon dans l’ensemble nous avons bien apprécié  notre  première journée de ski avec M. Chapon, M. Migotto, Mme Valente, Mr Capiaux et les moniteurs de ski avec une fin de journée heureuse et bien méritée de par les efforts fournis pour apprendre ce nouveau sport et pouvoir en profiter plus tard.  (ROUCARD Hadrien et FERNANDES Paolo)</p> <br><br>
							<h5>Mercredi 16/01/2019</h5>
							<p>Le matin, en ce qui concerne les TU, nous sommes allés au Cetim Ctdec de la ville de Cluses. Nous sommes arrivés à 9 heures. Mr Frédéric, nous a accueillis dans son entreprise, pour nous montrer ce qu’elle fait, ce qu’elle fabrique et exporte. Avec leurs machines traditionnelles, tours, fraiseuses,  nous avons pu essayer de faire une pièce manuellement (casse-tête).  (FAFI Jalal) <br><br>

							En ce qui concerne les MEI, nous avons pris la direction La Fonderie du Mont Blanc. Cette société fonctionne à l’économie circulaire c’est-à-dire, que leurs copeaux se recyclent en Espagne pour les refondre en lingots. Ces lingots servent à fabriquer de nouvelles roulottes de laiton, afin de les usiner de nouveau. (AMIRAT Jebril) <br><br>

							L’après-midi, nous sommes allés à l’entreprise DGIN. Nous sommes partis avec un groupe constitué de 2nde Mei  et 2nde TU alors que d’autres élèves sont allés à CTDEC qui est une entreprise de décolletage. (DIN Maxime et ZEGGAOUI Faredj) <br><br>

							Jeudi 17/01/2019

							Cette journée était consacrée à la pratique du ski. Le groupe des confirmés est parti avec Mr BUZON du côté de Morillon tandis que les débutants, continuaient à s’exercer sur des pistes vertes ou bleues avec leur moniteur. Au déjeuner, nous avons déguster une tartiflette.</p>
