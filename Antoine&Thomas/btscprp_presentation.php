				<h2><i class="fas fa-dot-circle"></i> Objectifs de la formation</h2><br></br>
<p>CPRP : Conception des Processus de Réalisation de Produits<br><br>
   Options: Production Unitaire ou Production Sérielle<br><br>
   Le technicien supérieur CPRP est un spécialiste des procédés de fabrication de pièces mécaniques, notamment par usinage. Ces pièces peuvent être destinées à des machines industrielles ou à des biens de consommation pour le grand public.<br></br>
   Le titulaire du BTS peut concevoir les processus de fabrication et intervenir tout au long de la chaîne de production, d’assemblage et de contrôle. De par la formation polyvalente en productique qu’il a reçue, il est rompu aux méthodes de conception, de fabrication et de gestion de production assistées par ordinateur et peut travailler dans tout service (méthodes, production, qualité, recherche) lié à la production industrielle.<br></br>
   Les principaux objectifs de ce BTS sont :<br></br>
   Préparer, dans les meilleures conditions, l’insertion professionnelle des futurs diplômés  en privilégiant l’acquisition de bases techniques et la maîtrise des logiciels de conception. <br></br>
   Offrir aux futurs diplômés de réelles possibilités de poursuite d’études après le BTS. Plus de 50% d’entre eux poursuivent leurs cursus.<br></br>
</p>
