				<h2><i class="fas fa-unlock-alt"></i>Les débouchés</h2><br></br>
<p>Le contexte professionnel du titulaire du brevet de technicien supérieur en Ingénierie des Procédés Avancés de Production dépend de l’importance des productions assurées par l’entreprise :<br></br>
  Dans le cadre des productions continues ou en séries renouvelables, il intervient au niveau de la conception des produits, de la préindustrialisation, en ingénierie collaborative, de l’industrialisation des produits (conception des processus et des outillages), de la qualification des produits, du lancement et du suivi des productions. <br></br>
  Pour les fabrications en petites séries non renouvelables, il coordonne les activités de la mise en production afin de réaliser des pièces ou des produits conformes dans les délais les plus brefs et à moindre coût.<br></br>
  Le titulaire du BTS CPRP peut occuper des emplois variés selon la poursuite ou non de ses études tels que :<br></br>
  Technicien Méthodes / Qualité / Sécurité, dessinateur industriel, technicien support technique, chef d’équipe d’une unité de production, responsable projet, conseiller Commercial en solutions de CFAO, ingénieur méthodes et développement industriel, professeur Certifié de Génie Mécanique, ingénieur Production.</p>
