						<div class="card-body">
							<h5>Mardi 02 Avril 2019</h5>
							<p>Ce matin, en raison d’un problème de car, nous avons quitté le lycée avec une heure et demi de retard. Après avoir déjeuné sur le trajet, nous nous sommes rendus directement au parc West adventure de Reviers. Au programme : parcours d’accrobranche et tyroliennes géantes ! Ceux qui étaient fatigués ont joué avec Gus, le chien du gérant (et oui ). Après quatre heures à crapahuter dans les arbres, nous avons repris la route vers Seignolles, où nous logeons. Nous nous sommes installés,  puis avons dîné. Ensuite, nous avons eu un temps libre : nous avons joué au loup (et oui bis ). Mais vous vous demandez sans doute quel temps nous avons eu ? Miraculeusement, après une arrivée sous la grêle, le soleil a pointé son nez, et nous a accompagné jusqu’à la fin de journée. Pourvu que ça dure !</p> <br><br>
							<h5>Mercredi 03 Avril 2019</h5>
							<p>Aujourd’hui, nous avons passé la matinée au gîte. Après le petit déjeuner, nous avons rédigé nos carnets de voyage, puis nous avons monté et décoré nos cerfs-volants. Le concours du plus beau cerf-volant a été remporté haut la main par Nowal, sous les applaudissements de ses camarades. Malgré la pluie et le froid, nous avons fait voler nos engins un long moment dans le parc. Après le déjeuner, nous nous sommes rendus au mémorial de Caen. Avant d’entrer, les élèves de 3e jaune ont présenté à ceux de l’autre classe la sculpture ”Non-Violence”, étudiée en classe. Puis, en groupe, nous avons visité le musée. Nous avons dû ouvrir l’oeil pour pouvoir compléter le livret pédagogique fourni par le Mémorial ! Au moment de descendre dans le bunker, une énorme averse de grêle s’est abattue sur le site. L’occasion, une fois la visite achevée, de se livrer à une mini bataille de boules de neige, histoire de se défouler un peu. Nous avons ensuite assisté à la projection d’un film sur le débarquement. Enfin, nous avons participé à un atelier, ”Une famille pendant la guerre”. Nous devions, en nous aidant de documents et d’objets d’époque, reconstituer la vie quotidienne d’une famille française tout au long de la Deuxième guerre mondiale. Nous rentrons au gîte, puis dînons. Ce soir, nous nous couchons tôt, car nous reprenons nos aventures aux aurores demain matin. En espérant que le temps soit plus clément qu’aujourd’hui !</p> <br><br>
							<h5>Jeudi 04 Avril 2019</h5>
							<p>Ce matin, nous avons décollé à 8 heures du gîte pour nous rendre au Mémorial de Caen, où nous avons récupéré un guide. Pendant le trajet pour la batterie allemande de Longues-sur-mer, il nous a raconté tout un tas d’anecdotes sur le D-Day. Nous avons donc visité la batterie, qui n’est pas un instrument de musique, mais un gros canon Skoda protégé par un bunker et dirigé vers la mer, servant de ligne défensive aux Allemands, avant d’être prise par les Britanniques. Il faisait beau, mais froid. Nous avons ensuite repris le car pour nous rendre au cimetière américain de Colleville, toujours accompagné de notre guide qui nous a raconté l’histoire de plusieurs soldats enterrés sur place. La vue de ces 9388 tombes surplombant la plage d’Omaha était impressionnante. Mais nous étions congelés ! Retour ensuite au Mémorial, où nous avons laissé notre guide et récupéré nos paniers repas. Notre chauffeur de bus, compte tenu des conditions climatiques, nous a autorisé à manger au chaud, dans le car. Nous nous sommes ensuite dirigés vers Ouistreham pour le char à voile. Une fois changés et équipés de nos casques, les moniteurs nous ont expliqué comment manier les chars. Il y avait du vent, certains se levaient ! Il y a eu quelques chutes, dont une très impressionnante de Matthieu, mais heureusement rien de grave, sinon un genou éraflé. Cela ne l’a pas empêché de repartir quelques minutes plus tard ! La météo s’est un peu radoucie (heureusement !), et nous avons même eu quelques éclaircies sur la plage. Ensuite, nous avons eu un temps libre dans la rue piétonne de Ouistreham, où nous avons pu acheter des souvenirs. Nous sommes rentrés au gîte et nous avons commencé à ranger nos affaires, puis nous avons dîné. Nous avons encore pu jouer un peu dehors, avant d’aller nous coucher.</p> <br><br>
							<h5>Vendredi 05 Avril 2019</h5>
							<p>
							Notre périple s’achève. <br><br>
							Ce matin, nous avons pris notre petit déjeuner à 8 heures, puis nous avons terminé de ranger nos affaires et nettoyé nos chambres. Pendant une heure, nous avons poursuivi la rédaction de notre carnet de voyage. À 10h30, après avoir dit au revoir aux propriétaires du gîte, nous sommes montés dans le car, direction Caen. La circulation dans la ville était compliquée, en raison des travaux d’installation du tramway, mais nous avons tout de même pu nous rendre au château, où le car nous a déposés. Nous avons eu alors une heure de quartier libre, avant de reprendre la route. Vers 13h30, nous nous sommes arrêtés sur une aire d’autoroute pour déjeuner. Nous entamons maintenant la dernière ligne droite. Au moment où nous envoyons ce message, nous sommes dans le car. Le GPS indique une arrivée aux alentours de 16h30. <br><br>
							Les élèves n’ont pas très envie de rentrer à Dugny, les enseignants un peu plus, la fatigue commençant à se faire sentir… Toutefois, ils sont ravis du séjour et fiers de leurs élèves, qui ont fait preuve pendant quatre jours d’une attitude exemplaire et se sont montrés enthousiastes et motivés par chacune des activités qui leur a été proposée. C’était un super voyage qui nous laissera à tous d’excellents souvenirs !
							</p>
