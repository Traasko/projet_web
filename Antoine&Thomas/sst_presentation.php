				<p>La formation de 12h permet de :<br></br>
          - maîtriser la conduite à tenir et les gestes de premiers secours (mettre en sécurité la personne accidentée, réagir face à un saignement ou un étouffement, mettre en PLS, utiliser un défibrillateur…), <br></br>
          - savoir qui et comment alerter,<br><br>
          - repérer les situations dangereuses dans son entreprise et savoir à qui et comment relayer ces informations dans l’entreprise,<br></br>
          - participer éventuellement à la mise en œuvre d’actions de prévention et de protection.<br></br></p>
