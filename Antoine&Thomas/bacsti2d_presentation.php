				<p>Au cours de l’année de terminale, les élèves développent un projet par équipe de 3 à 5 élèves. Au travers de ce projet, les élèves doivent s’organiser pour apporter une solution technique à un problème posé. L’équipe doit planifier les principales phases du projet dont l’objectif est la mise en œuvre, la modification et/ou l’amélioration d’un système. Début février, chaque équipe choisit un sujet et un cahier des charges partiels. <br><br>

