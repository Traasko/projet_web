				<h2><i class="fas fa-dot-circle"></i> Objectifs de la formation</h2><br></br>
				<p>Le bac technologique STI2D s’adresse à ceux qui s’intéressent à l’ingénierie industrielle, à l’innovation technologique et à la préservation de l’environnement. Il prépare à la poursuite d’études scientifiques et technologiques industrielles en BTS, DUT, classes préparatoires technologies et sciences industrielles (TSI), puis écoles d’ingénieurs, ou écoles spécialisées (électronique, réseaux, matériaux, bâtiment…). <br><br>

				Plus des deux tiers des enseignements sont consacrés aux matières scientifiques et technologiques de la filière et de la spécialité. La conception de produit est au cœur des enseignements de la spécialité ITEC tandis que la spécialité SIN propose de l’informatique et de la programmation. Les enseignements portent sur l’analyse et la conception de produits dans le respect d’une démarche de développement durable. <br><br>

				L’enseignement de chaque option, quand il ne se déroule pas dans le cadre d’un projet se déroule sous la forme de travaux pratiques. Ces TP ont pour objectifs de développer l’autonomie des élèves tout en leur apportant l’aide pédagogique dont ils ont besoin.</p>
				<h4>OPTION SIN</h4>
				<p>Avec cette option, les élèves travaillent sur divers systèmes numériques et différents systèmes d’exploitation. <br>
				L’enseignement de l’option SIN  se fait dans le cadre de différents projets. Les élèves sont conduits à mener des travaux concrets, passant par des phases de création et de conception. <br><br>
				Cette option aborde différents sujets qui sont récurrents dans les grands secteurs d’activités de la filière électronique, tels que : Création de site web, structure d’un réseau, systèmes de télécommunications, programmation structurée … <br><br>

				Les TP sont divers et sollicitent différents matériels high-tech ou électroniques.</p>
				<h4>OPTION ITEC</h4>
				<p>La spécialité Innovation Technologique et Eco-Conception (ITEC) explore l’étude et la recherche de solutions techniques innovantes relatives aux produits manufacturés en intégrant la dimension design et ergonomie. Elle apporte les compétences nécessaires à l’analyse, l’éco-conception et l’intégration dans son environnement d’un système dans une démarche de développement durable. <br><br>

				Les différents supports utilisés sont les systèmes didactiques d’expérimentation des procédés (usinage, impression en 3D, mise en forme), les équipements de mesures (métrologie) et d’ informatiques (progiciels de simulation et de réalité virtuelle)</p>
